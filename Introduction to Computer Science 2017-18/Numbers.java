public class Numbers {
	public static void main (String[]args) {

		double d = 7.0;
		int i = 2;

		System.out.print("d / i = " +(int)d/i + "\n\n");
		System.out.print("d / i = " + d/i + "\n\n");
		System.out.print("d + i = " + (d+i) + "\n\n");
		System.out.print("d - i = " +(int)(d-i) + "\n\n");
		System.out.print("d mod i = " +(int)d%i + "\n\n");

}
}